/** بذرةُ اليوم (ثابتة خلال اليوم، متبدّلة عبر الأيام). */
export function seedOfDay(now = new Date()): string {
  const start = new Date(now.getFullYear(), 0, 0);
  const day = Math.floor((now.getTime() - start.getTime()) / 86400000);
  return DAILY_SEEDS[day % DAILY_SEEDS.length];
}

/** مرحلة نموّ «المداومة» كاستعارة طبيعية تناسب الثيم الهادئ. */
export interface GrowthStage {
  emoji: string;
  label: string;
}

export function streakStage(count: number): GrowthStage {
  if (count >= 100) return { emoji: "🌳", label: "شجرة وارفة" };
  if (count >= 30) return { emoji: "🪴", label: "نبتة راسخة" };
  if (count >= 7) return { emoji: "🌿", label: "غصنٌ يخضرّ" };
  if (count >= 1) return { emoji: "🌱", label: "برعم" };
  return { emoji: "", label: "" };
}
