const MILESTONES = [7, 30, 100, 365];

/** هل هذا اليوم محطّة احتفال؟ */
export function isMilestone(days: number): boolean {
  return MILESTONES.includes(days);
}

/** رسالة المحطّة عند بلوغها (وإلا null). */
export function milestoneMessage(days: number): string | null {
  switch (days) {
    case 7:
      return "سبعةُ أيامٍ متتالية — بدأت العادةُ تترسّخ.";
    case 30:
      return "ثلاثونَ يوماً! صارت من نسيج يومك.";
    case 100:
      return "مئةُ يومٍ من الثبات — إنجازٌ يستحقّ الفخر.";
    case 365:
      return "عامٌ كامل. أنت اليومَ غيرُ من بدأ.";
    default:
      return null;
  }
}

