const MILESTONES = [7, 30, 100, 365];

/** هل هذا اليوم محطّة احتفال؟ */
export function isMilestone(days: number): boolean {
  return MILESTONES.includes(days);
}

/** رسالة المحطّة عند بلوغها (وإلا null) — تتصاعد هيبتُها مع المسافة. */
export function milestoneMessage(days: number): string | null {
  switch (days) {
    case 7:
      return "سبعةُ أيامٍ متّصلة. لم تعد نيّةً — صارت عادةً تعرف طريقها إليك.";
    case 30:
      return "ثلاثون يوماً. ما بدأ قراراً صار خيطاً في نسيج أيامك.";
    case 100:
      return "مئةُ يوم. هذا ليس حماساً عابراً؛ هذا طبعٌ بنيتَه بيدك.";
    case 365:
      return "عامٌ كامل، يوماً فوق يوم. قليلون من يَفُون لأنفسهم هكذا — وأنت منهم.";
    default:
      return null;
  }
}

