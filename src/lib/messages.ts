/**
 * رسالة إتمام مخصّصة بالسياق (الاسم + وقت اليوم). تجعل التشجيع «مرئياً»
 * للمستخدم أكثر من عبارةٍ عامّة، وهو ما يرفع الارتباط.
 */
export function contextualCompletion(name: string | null, hour: number): string {
  const t = hour < 12 ? "هذا الصباح" : hour < 17 ? "هذه الظهيرة" : "هذا المساء";
  const n = name ? `${name}، ` : "";
  const options = [
    `${n}أنجزتَها ${t} بلا ضجيج.`,
    `${n}وعدٌ صغيرٌ وفيتَ به ${t}.`,
    `${n}خطوةٌ ${t}، وأثرٌ يتراكم.`,
    name ? `وفيتَ لنفسك ${t} يا ${name}.` : `وفيتَ لنفسك ${t}.`,
    `${n}هكذا يُبنى الثبات — ${t} أيضاً.`,
  ];
  return options[Math.floor(Math.random() * options.length)];
}

